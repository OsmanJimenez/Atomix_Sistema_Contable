    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
   if(n.getText().isEmpty() && c.getText().isEmpty() && cr.getText().isEmpty()){
   JOptionPane.showMessageDialog(null, "Algunos campos estan vacios");
   }else{
        conexion con=new conexion();
        String a,b,d,e,f,g;
        b=c.getText();
        f=ced.getText();
        d=cr.getText();
        a=n.getText();
        e=""+r.getSelectedIndex();
        g=tel.getText();
        String sql="";
        Connection reg =con.getConnection();
        sql="Insert Into user(Nombre,Contrasena,Correo,Rol,Cedula,Telefono) Values (?,?,?,?,?,?)";
        try{
            PreparedStatement pst=reg.prepareStatement(sql);
            pst.setString(1, a);
            pst.setString(2, b);
            pst.setString(3, d);
            pst.setInt(4, Integer.parseInt(e));
            pst.setInt(5,Integer.parseInt(f));
            pst.setInt(6,Integer.parseInt(g));
            pst.execute();
            
            JOptionPane.showMessageDialog(null, "Registro exitoso");
        }  catch (SQLException ex) {
            Logger.getLogger(Configuracion_Usuario_Modificar.class.getName()).log(Level.SEVERE, null, ex);
        }
          catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Algunos campos con valores numericos tienen datos invalidos");
        }
          catch (NullPointerException ex ) {
             JOptionPane.showMessageDialog(null, "Algunos campos estan vacios");
        }
   
   
   }

    }//GEN-LAST:event_jButton1ActionPerformed

